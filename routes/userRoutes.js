const express = require('express');
const router = express.Router();
const {
    createUser, 
    getUsers, 
    getUser, 
    updateUser, 
    deleteUser
} = require('../controllers/userController');

// REMOVE THESE LINES:
// require('../swagger/schemas/userSchemas');
